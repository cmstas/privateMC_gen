# Make voms proxy
voms-proxy-init --voms cms --out $(pwd)/voms_proxy.txt --hours 4
export X509_USER_PROXY=$(pwd)/voms_proxy.txt

export SCRAM_ARCH=slc7_amd64_gcc530

source /cvmfs/cms.cern.ch/cmsset_default.sh
if [ -r CMSSW_8_0_33_UL/src ] ; then
  echo release CMSSW_8_0_33_UL already exists
else
  scram p CMSSW CMSSW_8_0_33_UL
fi
cd CMSSW_8_0_33_UL/src
eval `scram runtime -sh`

scram b
cd ../..

# Maximum validation duration: 28800s
# Margin for validation duration: 30%
# Validation duration with margin: 28800 * (1 - 0.30) = 20160s
# Time per event for each sequence: 0.3639s
# Threads for each sequence: 8
# Time per event for single thread for each sequence: 8 * 0.3639s = 2.9112s
# Which adds up to 2.9112s per event
# Single core events that fit in validation duration: 20160s / 2.9112s = 6924
# Produced events limit in McM is 10000
# According to 1.0000 efficiency, validation should run 10000 / 1.0000 = 10000 events to reach the limit of 10000
# Take the minimum of 6924 and 10000, but more than 0 -> 6924
# It is estimated that this validation will produce: 6924 * 1.0000 = 6924 events
EVENTS=6924


# cmsDriver command
cmsDriver.py  --python_filename HIG-RunIISummer20UL16HLT-00281_1_cfg.py --eventcontent RAWSIM --outputCommand "keep *_mix_*_*,keep *_genPUProtons_*_*" --customise Configuration/DataProcessing/Utils.addMonitoring --datatier GEN-SIM-RAW --inputCommands "keep *","drop *_*_BMTF_*","drop *PixelFEDChannel*_*_*_*" --fileout file:HIG-RunIISummer20UL16HLT-00281.root --conditions 80X_mcRun2_asymptotic_2016_TrancheIV_v6 --customise_commands 'process.source.bypassVersionCheck = cms.untracked.bool(True)' --step HLT:25ns15e33_v4 --geometry DB:Extended --filein file:HIG-RunIISummer20UL16DIGIPremix-00279.root --era Run2_2016 --no_exec --mc -n $EVENTS --nThreads 1 || exit $? ;
